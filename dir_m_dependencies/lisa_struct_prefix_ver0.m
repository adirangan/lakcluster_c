function l = lisa_struct_prefix_ver0(l,dir_code,dir_trunk);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%;
% store directories with code. ;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%;
l.dir_code = dir_code;
l.dir_trunk = dir_trunk;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% ;
% select parameters used during analysis. ;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% ;
[l.study_trunk_,l.study_name_,l.n_study] = lisa_define_study_ver0(l.cl_num);
l.Ireq_half = floor(l.n_study/2); l.Icat_full = l.n_study;
l.string_prefix = sprintf('PGC_cl%d_maf01',l.cl_num);
%%%%%%%%%%%%%%%% ;
[l.maf_lo_threshold,l.maf_hi_threshold] = lisa_set_maf_xx_threshold(l.n_maf);
%%%%%%%%%%%%%%%% ;
[l.Ireq,l.Icat,l.mds_used_,l.mds_repl] = lisa_set_mds_used(l.n_cov);
%%%%%%%%%%%%%%%% ;
%n_shuffle typically 64; n_scramble typically 0 (for first bicluster) or 1 (for the second bicluster);
l.dir__in = sprintf('%s/dir_%s',l.dir_trunk,l.string_prefix);
l.dir_out = sprintf('%s/dir_%s_analyze',l.dir_trunk,l.string_prefix);
if (l.verbose>1); disp(sprintf(' %% dir__in: %s',l.dir__in)); end;
if (l.verbose>1); disp(sprintf(' %% dir_out: %s',l.dir_out)); end;
