function test_loader_plot_C_absZ__0(AB_,C_VariableName_LC_,dir_trunk,infix);

n_CCOV = AB_.n_CCOV;
n_CLabel = AB_.n_CLabel;
n_s = (n_CLabel-1)*n_CLabel/2;

[tmp_U_,tmp_S_,tmp_V_] = svds(AB_.AB_C_absZ_,1); [~,tmp_C_absZ_U_ij_] = sort(tmp_U_,'descend'); [~,tmp_C_absZ_V_ij_] = sort(tmp_V_,'descend');
