%%%%%%%%;
figure(1);clf;
colormap(colormap_beach());
imagesc(AB_.AB_C_absZ_(tmp_C_absZ_U_ij_,tmp_C_absZ_V_ij_),[0,+15]); colorbar();
xlim([1,n_CCOV]); ylim([1,n_s]);
set(gca,'XTick',1:n_CCOV,'XTickLabel',C_VariableName_LC_(tmp_C_absZ_V_ij_),'TickLabelInterpreter','none'); xtickangle(90);
set(gca,'YTick',[],'YTickLabel',[]);
xlim([1,n_CCOV]); ylabel('CLabel pairs (rearranged)'); title('Covariates arranged by relevance across all label-pairs');
set(gca,'FontSize',7); %set(gca,'FontName','Courier');
set(gcf,'Position',1+[0,0,1024*2,1024]);
fname_base = sprintf('%s/dir_jpg/AB_%s_.AB_C_absZ',dir_trunk,infix);
disp(sprintf(' %% writing %s.jpg',fname_base));
print('-djpeg',sprintf('%s.jpg',fname_base));
print('-depsc',sprintf('%s.eps',fname_base));
