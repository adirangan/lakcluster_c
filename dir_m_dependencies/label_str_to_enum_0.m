function label_enum_ = label_str_to_enum_0(label_str_);
% converts string labels to numeric labels, ;
% does not assume that the strings are themselves number. ;
% e.g., strings such as 'AA', 'ABBA0', etc. are allowed. ;
% these strings will be enumerated based on dictionary order. ;
n_u = numel(label_str_);
u_label_ = unique(label_str_);
n_label = length(u_label_);
n_label_ = zeros(n_label,1);
for nlabel = 1:n_label;
n_label_(nlabel) = length(find(strcmp(label_str_,u_label_(nlabel))));
end;%for nlabel = 1:n_label;
label_enum_ = zeros(n_u,1);
for nlabel=1:n_label;
tmp_ij_ = find(strcmp(label_str_,u_label_(nlabel)));
if (~isempty(tmp_ij_));
label_enum_(tmp_ij_) = nlabel;
end;%if (~isempty(tmp_ij_));
end;%for nlabel=1:n_label;

