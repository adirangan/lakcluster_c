function test_loader_plot_Z_absZ__0(AB_,dir_trunk,infix);

n_Z_GENE = AB_.n_Z_GENE;
n_CLabel = AB_.n_CLabel;
n_s = (n_CLabel-1)*n_CLabel/2;

[tmp_U_,tmp_S_,tmp_V_] = svds(AB_.AB_Z_absZ_,1,'largest','FailureTreatment','keep'); [~,tmp_Z_absZ_U_ij_] = sort(tmp_U_,'descend'); [~,tmp_Z_absZ_V_ij_] = sort(tmp_V_,'descend');
