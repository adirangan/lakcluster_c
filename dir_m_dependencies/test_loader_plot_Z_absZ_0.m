xlim([1,n_Z_GENE]); ylim([1,n_s]);
set(gca,'ZTick',[],'ZTickLabel',[]);
set(gca,'YTick',[],'YTickLabel',[]);
xlim([1,n_Z_GENE]); ylabel('CLabel pairs (rearranged)'); title('genes arranged by relevance across all label-pairs');
set(gca,'FontSize',7); %set(gca,'FontName','Courier');
set(gcf,'Position',1+[0,0,1024*2,1024]);
fname_base = sprintf('%s/dir_jpg/AB_%s_.AB_Z_absZ',dir_trunk,infix);
disp(sprintf(' %% writing %s.jpg',fname_base));
print('-djpeg',sprintf('%s.jpg',fname_base));
