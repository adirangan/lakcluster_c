function l = lisa_struct_studyindex(l);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% ;
% determine which studies correspond to which patients. ;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% ;
l.study_path_ = cell(l.n_study,1);
l.study_index_ = zeros(l.n_patient,1);
for ns=1:l.n_study;
l.study_path_{ns} = sprintf('/home/grouther/%s/%s.fam',l.study_trunk_{ns},l.study_name_{ns});
l.study_index_(find(strcmp(l.fam_dir_,l.study_path_{ns}))) = ns;
end;%for ns=1:l.n_study;
