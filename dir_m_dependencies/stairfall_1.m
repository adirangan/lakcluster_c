function [p,lim_] = stairfall_1(x_ord_,y_ord_,lim_,c_);
% plots y_ord_ as an opaque staircase. ;
% x_ord_ lists the edges of the stairs, thus n_x = n_y+1. ;
n_y = numel(y_ord_); n_row = n_y;
y_ord_ = reshape(y_ord_,[n_y,1]);
if (isempty(x_ord_)); x_ord_ = 0:n_y; end;
n_x = numel(x_ord_); assert(n_x==n_y+1);
x_ord_ = reshape(x_ord_,[n_x,1]);
if nargin<3; lim_ = []; end;
if nargin<4; c_ = []; end;
if (isempty(c_)); c_ = [1,0,0]; end;
if (isempty(lim_)); lim_ = [min(y_ord_,[],'all'),max(y_ord_,[],'all')]; end;
y_ord_ = max(lim_(1),y_ord_);
y_ord_ = min(lim_(2),y_ord_);
x_ = zeros(3+4*n_row,1);
y_ = zeros(3+4*n_row,1);
c___ = zeros(1,1,3);
c___(1,1,:) = c_(1,:);
x_ = zeros(1+2*n_row,1);
y_ = zeros(1+2*n_row,1);
na=0;
for nrow=0:n_row-1;
x_(1+na) = x_ord_(1+nrow+0);
y_(1+na) = y_ord_(1+nrow);
na=na+1;
x_(1+na) = x_ord_(1+nrow+1);
y_(1+na) = y_ord_(1+nrow);
na=na+1;
end;%for nrow=0:n_row-1;
for nrow=n_row-1:-1:0;
x_(1+na) = x_ord_(1+nrow+1);
y_(1+na) = min(lim_);
na=na+1;
x_(1+na) = x_ord_(1+nrow+0);
y_(1+na) = min(lim_);
na=na+1;
end;%for nrow=n_row-1:-1:0;
x_(1+na) = x_(1+0);
y_(1+na) = y_(1+0);
na=na+1;
p = patch(x_,y_,c___,'EdgeColor','k');
