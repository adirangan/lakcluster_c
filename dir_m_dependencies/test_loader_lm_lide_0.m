bar(1:n_rank,log10(sqrt(sum((ones(n_u,1)*beta_lide_E_un_(1,:) + E_lide_*beta_lide_E_un_(2:end,:)).^2,1))),'r');
xlabel('pc #'); ylabel('log10(l2-norm)');
title('numerical rank of beta_lide_E_','Interpreter','none');
end;%if flag_E;
if flag_I;
subplot(2,2,2);
bar(1:n_rank,sqrt(sum((ones(n_u,1)*beta_lide_I_un_(1,:) + I_lide_*beta_lide_I_un_(2:end,:)).^2,1)),'b');
xlabel('pc #'); ylabel('l2-norm');
