function output = snchoosek(n_,k_);
%%%%%%%%;
if (nargin<1);
k_ = 1:20; n_ = ceil(1.45*k_);
for nl=1:length(k_);
ln_1_(nl) = log(nchoosek(n_(nl),k_(nl)));
end;%for nl=1:length(k_);
ln_2_ = snchoosek(n_,k_);
subplot(1,2,1);
plot(k_,ln_1_,'k.-',k_,ln_2_,'ro-');
xlabel('k');ylabel('log(nchoosek)');
subplot(1,2,2);
plot(k_,log10(abs(ln_1_-ln_2_)./ln_1_),'kx-');
xlabel('k');ylabel('log10(relative error in log(nchoosek))');
disp('returning');return;
end;%if (nargin<1);
%%%%%%%%;
output = sfact(n_) - sfact(n_-k_) - sfact(k_);
tmp_ij_ = find(k_<0 | n_<0 | k_>n_);
output(tmp_ij_) = -Inf;
