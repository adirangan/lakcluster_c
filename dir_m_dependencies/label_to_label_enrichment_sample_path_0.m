function [lN__,lP__,cap_abst____] = label_to_label_enrichment_sample_path_0(n_label_A,n_label_A_,num_A_,n_label_B,n_label_B_,num_B_,lP_base,n_iteration,n_timestep,ctol);
%%%%%%%%;
if isempty(ctol); ctol = 1e-6; end;
%%%%%%%%;
verbose=1;
if (verbose); disp(sprintf(' %% [entering label_to_label_enrichment_sample_path_0] n_iteration %d n_timestep %d ctol %0.12f',n_iteration,n_timestep,ctol)); end;
lN__ = zeros(n_iteration,n_timestep);
lP__ = zeros(n_iteration,n_timestep);
if nargout>=3;
cap_abst____ = zeros(n_label_A,n_label_B,n_iteration,n_timestep);
end;%if nargout>=3;
%%%%%%%%;
X_rsum_ = zeros(n_label_B,n_label_A*n_label_B);
for nlabel_B=1:n_label_B;
tmp_ = zeros(n_label_A,n_label_B); tmp_(:,nlabel_B) = 1;
X_rsum_(nlabel_B,:) = reshape(tmp_,1,n_label_A*n_label_B);
end;%for nlabel_B=1:n_label_B;
y_rsum_ = zeros(n_label_B,1);
X_csum_ = zeros(n_label_A,n_label_A*n_label_B);
for nlabel_A=1:n_label_A;
tmp_ = zeros(n_label_A,n_label_B); tmp_(nlabel_A,:) = 1;
X_csum_(nlabel_A,:) = reshape(tmp_,1,n_label_A*n_label_B);
end;%for nlabel_A=1:n_label_A;
y_csum_ = zeros(n_label_A,1);
%%%%%%%%;
for niteration=1:n_iteration;
if (verbose); if (mod(niteration,10)==0); disp(sprintf(' niteration %d/%d',niteration,n_iteration)); end; end;
tmp_cap_ = sparse(num_A_,num_B_(randperm(numel(num_B_))),1,n_label_A,n_label_B);
tmp = sum(gammaln(1+tmp_cap_),'all');
ntimestep=1;
lN__(niteration,ntimestep) = +tmp; lP__(niteration,ntimestep) = lP_base - tmp;
if nargout>=3;
cap_abst____(:,:,niteration,ntimestep) = tmp_cap_;
end;%if nargout>=3;
for ntimestep=2:n_timestep;
if (verbose>1); if (mod(ntimestep,10)==0); disp(sprintf(' ntimestep %d/%d',ntimestep,n_timestep)); end; end;
[dcap_,~,cap_t_] = label_to_label_enrichment_constrain_gradient_0(n_label_A,n_label_A_,n_label_B,n_label_B_,tmp_cap_,X_rsum_,y_rsum_,X_csum_,y_csum_,ctol);
tmp = sum(gammaln(1+cap_t_),'all');
lN__(niteration,ntimestep) = tmp; lP__(niteration,ntimestep) = lP_base - tmp;
if nargout>=3;
cap_abst____(:,:,niteration,ntimestep) = cap_t_;
end;%if nargout>=3;
tmp_cap_ = cap_t_;
end;%for ntimestep=2:n_timestep;
end;%for niteration=1:n_iteration;
if (verbose); disp(sprintf(' %% [finished label_to_label_enrichment_sample_path_0] n_iteration %d n_timestep %d ctol %0.12f',n_iteration,n_timestep,ctol)); end;
